console.log("Latihan Looping\n");
for (var i = 1; i <= 10; i++) {
    console.log("Perulangan ke-" + i);
}

// Looping dengan While
var j = 1;
while (j <= 10) {
    console.log("Perulangan ke-" + j);
    j++;
}

// perulangan menggunakan kata javascript
var kata = "JavaScript";
for (var k = 0; k = kata.length; k++) {
    console.log("Huruf ke-" + (k + 1) + " adalah " + kata.charAt(k));
}

